package com.codename1.uikit.pheonixui.model;

public class Transfer {
    private String amount;
    private String date;

    public Transfer() {
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
